# Program demonstrating working with string
str = 'Hello Python'
print (str) # Prints complete string
print (str[0]) # Prints first character of the string
print (str[2:5]) # Prints charcters starting from 3rd to 5th
print (str[2:1]) # Prints string starting from 3rd character
print (str * 2) # Prints string two time
print (str + "PROGRAMMING") # Prints concatenated string
